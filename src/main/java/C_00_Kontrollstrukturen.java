/*
    Kontrollstrukturen in Java sind Anweisungen, die den Ablauf eines Programms steuern, indem sie entscheiden,
    ob und wie oft bestimmte Teile des Codes ausgeführt werden. Sie ermöglichen es einem Programm,
    auf unterschiedliche Daten und Zustände dynamisch zu reagieren und entsprechende Aktionen auszuführen.


    In Java gibt es verschiedene Arten von Kontrollstrukturen:


        Verzweigungen (Entscheidungsstrukturen):

        if:
        Die if-Anweisung führt einen Codeblock aus, wenn eine Bedingung wahr (true) ist. Es kann auch mit else erweitert werden,
        um alternative Aktionen auszuführen, wenn die Bedingung falsch (false) ist.

        else if:
        Eine Erweiterung der if-Anweisung, die es ermöglicht, mehrere Bedingungen nacheinander zu überprüfen.

        switch:
        Eine Alternative zur if-Anweisung, die das Programm verschiedene Aktionen ausführen lässt, basierend auf dem Wert einer Variable.
        switch ist nützlich, wenn viele Bedingungen auf denselben Wert geprüft werden müssen.


        Schleifen (Wiederholungsstrukturen):

        while:
        Führt einen Codeblock wiederholt aus, solange eine bestimmte Bedingung wahr ist.
        Die Bedingung wird vor dem ersten Durchlauf der Schleife geprüft.

        do-while:
        Ähnlich wie die while-Schleife, aber die Bedingung wird nach jedem Durchlauf der Schleife geprüft,
        wodurch sichergestellt wird, dass der Codeblock mindestens einmal ausgeführt wird.

        for:
        Eine Schleife, die besonders nützlich ist, wenn die Anzahl der Durchläufe im Voraus bekannt ist.
        Sie besteht aus drei Teilen: der Initialisierung, der Bedingung und der Inkrementierung/Veränderung.

        for-each:
        Eine spezialisierte Form der for-Schleife, die für Arrays und Sammlungen verwendet wird.
        Sie vereinfacht das Durchlaufen von Elementen, ohne einen Index explizit verwalten zu müssen.


        Sprunganweisungen:

        break:
        Wird verwendet, um eine Schleife vorzeitig zu beenden oder aus einem switch-Block herauszuspringen.

        continue:
        Überspringt den aktuellen Durchlauf einer Schleife und fährt mit dem nächsten Durchlauf fort.

        return:
        Beendet eine Methode und gibt optional einen Wert an den Aufrufer zurück.


    Diese Kontrollstrukturen ermöglichen die Erstellung dynamischer und flexibler Java-Programme,
    die auf verschiedene Eingaben und Situationen reagieren können.
    Die Wahl der geeigneten Kontrollstruktur hängt von der spezifischen Anforderung des zu lösenden Problems ab.
*/

